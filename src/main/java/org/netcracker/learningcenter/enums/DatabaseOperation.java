package org.netcracker.learningcenter.enums;

/**
 * Database operations
 *
 * @author Mikhail Savin
 */
public enum DatabaseOperation {

    /**
     * Add to database (insert)
     */
    ADD,

    /**
     * Remove from database (delete)
     */
    REMOVE,

    /**
     * Update in the database (update)
     */
    UPDATE,

    /**
     * Get all existing instances from database
     */
    GET_ALL
}
